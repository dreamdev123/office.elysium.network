                                                      <strong>"DOLLAR COST AVERAGING" A SMART WAY TO ENTER A POSITION!</strong><br>
                                                      <br>
                                                      Dollar-cost averaging (DCA) is an investment strategy in which an investor divides up the total amount to be invested across periodic or targeted purchases of a target asset in an effort to reduce the impact of volatility on the overall purchase.<br>
                                                      <br>
                                                      Example: You receive an <strong>INSIDER ALERT</strong> opinion that a temporally bottom for Bitcoin might be at 50K and you have $1000 to invest. You now can split that $1000 into 4 x $250 and invest only $250 at $50K, in case it gets lower to $48, you can invest another $250 at $48. You now wait for new opinions and if Bitcoin goes up to for instance 51K you invest another $250K etc.<br>
                                                      <br>
                                                      <strong>"LEVERAGED POSITIONS" A BAD WAY TO GET 'REKT'!</strong><br>
                                                      <br>
                                                      Leverage is the use of a smaller amount of capital to gain exposure to larger trading positions. A leverage of 10:1 means that in order to open and maintain a position, the necessary margin required is one tenth less than the transaction size. So, a trader would require $10,000 to enter a trade for $100,000. This is very dangerous when the assets go down you'll lose x 10! We do not endorse leverage as most people will lose. Stick to spot trading is our opinion!<br>
